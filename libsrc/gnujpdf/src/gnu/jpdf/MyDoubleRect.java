package gnu.jpdf;

/**
 *
 * @author JPEXS
 */
public class MyDoubleRect {

    public double xMin;
    public double yMin;
    public double xMax;
    public double yMax;

    public MyDoubleRect(double xMin, double yMin, double xMax, double yMax) {
        this.xMin = xMin;
        this.yMin = yMin;
        this.xMax = xMax;
        this.yMax = yMax;
    }

}
